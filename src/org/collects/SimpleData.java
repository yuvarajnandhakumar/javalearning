package org.collects;

public class SimpleData {

	private int age;
	private String name;
	private EmpPhno emp; //phno - mailid
	
	
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	public EmpPhno getEmp() {
		return emp;
	}
	public void setEmp(EmpPhno emp) {
		this.emp = emp;
	}
	
	

}
